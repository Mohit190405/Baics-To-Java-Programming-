public class Challenge04
{
    public static void main(String[] args) {

        // 1. Right-Angled Triangle Pattern
        System.out.println("*\n**\n***\n****\n*****");

        // 2. Square Pattern
        System.out.println("\n*****\n*****\n*****\n*****\n*****");

        // 3. Pyramid Pattern
        System.out.println("\n    *\n   ***\n  *****\n *******\n*********");

        // 4. Diamond Pattern
        System.out.println("\n    *\n   ***\n  *****\n   ***\n    *");

        // 5. Inverted Right-Angled Triangle Pattern
        System.out.println("\n*****\n****\n***\n**\n*");

        // 6. Right-Angled Triangle with Numbers Pattern
        System.out.println("\n1\n12\n123\n1234\n12345");

        // 7. Inverted Pyramid of Numbers Pattern
        System.out.println("\n12345\n1234\n123\n12\n1");
    }

}
